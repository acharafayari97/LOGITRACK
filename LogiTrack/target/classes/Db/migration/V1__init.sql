CREATE TABLE CLIENTI (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  RAGIONE_SOCIALE VARCHAR2(255 CHAR) NOT NULL,
  EMAIL          VARCHAR2(255 CHAR) UNIQUE NOT NULL,
  PARTITA_IVA    VARCHAR2(16 CHAR)  UNIQUE NOT NULL
);

CREATE TABLE PRODOTTI (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NOME                 VARCHAR2(255 CHAR) NOT NULL,
  CODICE               VARCHAR2(255 CHAR) UNIQUE NOT NULL,
  PREZZO_UNITARIO      NUMBER(13,2) NOT NULL,
  QUANTITA_DISPONIBILE NUMBER(10)   NOT NULL
);
